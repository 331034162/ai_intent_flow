from datetime import datetime, timezone, timedelta


# ============================================================
# 日期工具（普通函数，非 tool）
# ============================================================


def get_current_date() -> str:
    """获取当前中国时区日期

        Returns:
            str: 当前日期，格式为 YYYY-MM-DD（中国时区，UTC+8）
        """
    china_tz = timezone(timedelta(hours=8))
    return datetime.now(china_tz).strftime("%Y-%m-%d")


